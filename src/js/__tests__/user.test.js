import { sortCharacters } from '../user';

test('sort characters by health', () => {
    const characters = [
        { name: 'мечник', health: 10 },
        { name: 'маг', health: 100 },
        { name: 'лучник', health: 80 },
    ];

    const sorted = sortCharacters(characters);
    expect(sorted).toEqual([
        { name: 'маг', health: 100 },
        { name: 'лучник', health: 80 },
        { name: 'мечник', health: 10 },
    ]);
});
